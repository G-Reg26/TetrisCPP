	sf::Clock dClock;

	sf::Vector2f blockSize = sf::Vector2f(window.getSize().x / 12.0f, window.getSize().y / 22.0f);

	Game game = Game(board, boardHeight, boardWidth, blockSize);

	while (window.isOpen())
	{
		sf::Time dTime = dClock.restart();

		sf::Event event;
		while (window.pollEvent(event))
		{
			if (event.type == sf::Event::Closed)
				window.close();
		}

		game.update(dTime.asSeconds());

		window.clear();
		game.draw(&window);
		window.display();
	}

